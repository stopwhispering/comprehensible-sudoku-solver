def parse_sudoku(sudoku: tuple) -> Dict[Tuple[int, int], int]:  # y, x -> candidate
    prefilled = {}
    assert (len(sudoku) == 9)
    for i, row in enumerate(sudoku, 1):
        for x, c in enumerate(row, 1):
            if c in '123456789':
                y = i
                prefilled[(y, x)] = int(c)

    return prefilled
